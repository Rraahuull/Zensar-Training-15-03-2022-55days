public class conditionalStatement {
    public static void main(String[] args) {
        int a = 5;
        int b = 5;

        if(a > b) {
            System.out.println("a is greater");
        } else if(a < b) {
            System.out.println("b is greater");
        } else {
            System.out.println("both are equal");
        }
    }
}
